namespace Microsoft.FluentUI.AspNetCore.Components;

/// <summary>
/// The visual appearance of a component.
/// </summary>
public enum ButtonAppearance
{
    /// <summary>
    /// Uses the current accent color
    /// </summary>
    Primary,

    /// <summary>
    /// Neutral
    /// </summary>
    Secondary,

    /// <summary>
    ///  Reveal on hover
    /// </summary>
    Subtle,

    /// <summary>
    /// Show a border.
    /// </summary>
    Outline,

    /// <summary>
    /// Transparent
    /// </summary>
    Transparent
}
